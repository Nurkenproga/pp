#sort list alphabetically:
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)

#sort list numeracli
thislist = [100, 50, 65, 82, 23]
thislist.sort()
print(thislist)

#reversing
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort(reverse = True)
print(thislist)

#same wtih numbers
thislist = [100, 50, 65, 82, 23]
thislist.sort(reverse = True)
print(thislist)